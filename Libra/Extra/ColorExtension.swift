import SwiftUI

extension Color {
    public static var maroon: Color {
        return Color(UIColor(red: 128/255, green: 0/255, blue: 0/255, alpha: 1.0))
    }
    
    public static var darkSlateGray: Color {
        return Color(UIColor(red: 47/255, green: 79/255, blue: 79/255, alpha: 1.0))
    }

}
